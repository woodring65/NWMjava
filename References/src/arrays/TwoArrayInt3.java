package arrays;
/*
 * 2차원 배열
 */
public class TwoArrayInt3 {

	public static void main(String[] args) {
		int[][] td = new int[2][3];
		
		System.out.printf("td[%d]\n", td[0][0]);
		System.out.printf("td[%d]\n", td[0][1]);
		System.out.printf("td[%d]\n", td[0][2]);
		
		System.out.printf("td[%d]\n", td[1][0]);
		System.out.printf("td[%d]\n", td[1][1]);
		System.out.printf("td[%d]\n", td[1][2]);

	}

}
