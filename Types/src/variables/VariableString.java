/*
 * 문자열(String)
 * 문자들의 집합
 * 큰따옴표(")로 양쪽을 묶어서 기술한다.
 */
public class VariableString {

	public static void main(String[] args) {
		String koreae = "korea";
		String koreah = "대한민국";
		String welcome = "한국에\t온 것을\t환영합니다.\n즐거운 여행되세요.-\"Korea\"";
		
		System.out.println(koreae);
		System.out.println(koreah);
		System.out.println(welcome);
	}
}
